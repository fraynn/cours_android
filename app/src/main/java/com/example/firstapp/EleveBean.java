package com.example.firstapp;

public class EleveBean {
    private String name, firstName;

    public EleveBean() {
    }

    public EleveBean(String name, String firstName) {
        this.name = name;
        this.firstName = firstName;
    }

    public String getName() {
        return name;
    }

    public String getFirstName() {
        return firstName;
    }

}
